    public static double hitung(int baris, int param) {
        double a, b, c, d;
        a = Double.parseDouble(String.valueOf(tabel_data.getValueAt(baris, 1)));
        b = Double.parseDouble(String.valueOf(tabel_data.getValueAt(baris, 2)));
        c = Double.parseDouble(String.valueOf(tabel_data.getValueAt(baris, 3)));
        d = 0;

        if (param == 1) {
            d = a * a;
        } else if (param == 2) {
            d = b * b;
        } else if (param == 3) {
            d = c * c;
        } else if (param == 4) {
            d = a * c;
        } else if (param == 5) {
            d = b * c;
        } else if (param == 6) {
            d = a * b;
        }

        return d;
    }

