    private void btn_hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hitungActionPerformed
        // TODO add your handling code here:
        int jumlah_data;
        double sigma_x1, sigma_x2, sigma_y, sigma_x1x1, sigma_x2x2, sigma_yy, sigma_x1y, sigma_x2y, sigma_x1x2,
                hitung_3a, hitung_3b, hitung_4a, hitung_5a, hitung_penyebut;
        sigma_x1 = 0;
        sigma_x2 = 0;
        sigma_y = 0;
        sigma_x1x1 = 0;
        sigma_x2x2 = 0;
        sigma_yy = 0;
        sigma_x1y = 0;
        sigma_x2y = 0;
        sigma_x1x2 = 0;
        jumlah_data = 0;

        for (int i = 0; i < 20; i++) {
            for (int j = 1; j < 7; j++) {
                tabel_data.setValueAt(hitung(i, j), i, (j + 3));
            }
        }

        for (int i = 0; i < 20; i++) {
            if ((double) tabel_data.getValueAt(i, 1) != 0 && (double) tabel_data.getValueAt(i, 2) != 0 && (double) tabel_data.getValueAt(i, 3) != 0) {
                sigma_x1 = sigma_x1 + (double) tabel_data.getValueAt(i, 1);
                sigma_x2 = sigma_x2 + (double) tabel_data.getValueAt(i, 2);
                sigma_y = sigma_y + (double) tabel_data.getValueAt(i, 3);
                sigma_x1x1 = sigma_x1x1 + (double) tabel_data.getValueAt(i, 4);
                sigma_x2x2 = sigma_x2x2 + (double) tabel_data.getValueAt(i, 5);
                sigma_yy = sigma_yy + (double) tabel_data.getValueAt(i, 6);
                sigma_x1y = sigma_x1y + (double) tabel_data.getValueAt(i, 7);
                sigma_x2y = sigma_x2y + (double) tabel_data.getValueAt(i, 8);
                sigma_x1x2 = sigma_x1x2 + (double) tabel_data.getValueAt(i, 9);
                jumlah_data++;
            }
        }

        jumlah_x1.setText(String.valueOf(sigma_x1));
        jumlah_x2.setText(String.valueOf(sigma_x2));
        jumlah_y.setText(String.valueOf(sigma_y));
        jumlah_x1x1.setText(String.valueOf(sigma_x1x1));
        jumlah_x2x2.setText(String.valueOf(sigma_x2x2));
        jumlah_yy.setText(String.valueOf(sigma_yy));
        jumlah_x1y.setText(String.valueOf(sigma_x1y));
        jumlah_x2y.setText(String.valueOf(sigma_x2y));
        jumlah_x1x2.setText(String.valueOf(sigma_x1x2));
        data_jumlah.setText(String.valueOf(jumlah_data));

        hitung_penyebut = (jumlah_data * sigma_x1x1 - Math.pow(sigma_x1, 2)) * (jumlah_data * sigma_yy - Math.pow(sigma_y, 2));
        hitung_3a = ((jumlah_data * sigma_x1y) - (sigma_x1 * sigma_y)) / Math.sqrt(hitung_penyebut);
        a3.setText(String.valueOf(df.format(hitung_3a)));
        
        hitung_penyebut = (jumlah_data * sigma_x2x2 - Math.pow(sigma_x2, 2)) * (jumlah_data * sigma_yy - Math.pow(sigma_y, 2));
        hitung_3b = ((jumlah_data * sigma_x2y) - (sigma_x2 * sigma_y)) / Math.sqrt(hitung_penyebut);
        b3.setText(String.valueOf(df.format(hitung_3b)));
        
        hitung_penyebut = (jumlah_data * sigma_x1x1 - Math.pow(sigma_x1, 2)) * (jumlah_data * sigma_x2x2 - Math.pow(sigma_x2, 2));
        hitung_4a = ((jumlah_data * sigma_x1x2) - (sigma_x1 * sigma_x2)) / Math.sqrt(hitung_penyebut);
        a4.setText(String.valueOf(df.format(hitung_4a)));
        
        hitung_penyebut = (1 - Math.pow(hitung_3a, 2)) * (1 - Math.pow(hitung_4a, 2));
        hitung_5a = (hitung_3b - (hitung_3a * hitung_4a)) / Math.sqrt(hitung_penyebut);
        a5.setText(String.valueOf(df.format(hitung_5a)));
    }//GEN-LAST:event_btn_hitungActionPerformed

